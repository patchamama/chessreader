import { useCallback, useEffect, useRef, useState } from 'react'
import {
  useSettingsStore,
  BOARD_THEMES,
  PIECE_THEMES,
  type BoardTheme,
  type PieceTheme,
} from '../../../shared/settings/settingsStore'
import { regenerateDiagram } from '../../../shared/chess/diagramApi'
import { formatDiagramEvalFooter, type DiagramEval } from '../../../shared/chess/diagramEvalFooter'

interface Props {
  src: string
  alt: string
  /** Position inferred from the preceding mainline (or initial position). */
  fen: string
}

/**
 * A book diagram image that can be toggled between the original (raster) image
 * and an autogenerated board rendered by the backend, which also carries a small
 * Stockfish evaluation footer. Right-clicking the generated board opens a config
 * menu (board theme / piece set / coordinates) and a FEN editor.
 */
export function DiagramImage({ src, alt, fen }: Props) {
  const boardTheme = useSettingsStore((s) => s.diagramBoardTheme)
  const pieceTheme = useSettingsStore((s) => s.diagramPieceTheme)
  const coordinates = useSettingsStore((s) => s.diagramCoordinates)
  const setSetting = useSettingsStore((s) => s.set)

  const [mode, setMode] = useState<'original' | 'generated'>('original')
  const [svg, setSvg] = useState<string | null>(null)
  const [evalData, setEvalData] = useState<DiagramEval | null>(null)
  const [loading, setLoading] = useState(false)
  const [error, setError] = useState<string | null>(null)
  const [menuOpen, setMenuOpen] = useState(false)
  const [editingFen, setEditingFen] = useState(false)
  const [activeFen, setActiveFen] = useState(fen)
  const [fenDraft, setFenDraft] = useState(fen)

  const regenerate = useCallback(
    async (overrideFen?: string) => {
      const useFen = overrideFen ?? activeFen
      setLoading(true)
      setError(null)
      try {
        const theme = BOARD_THEMES[boardTheme]
        const res = await regenerateDiagram({
          fen: useFen,
          lightColor: theme.light,
          darkColor: theme.dark,
          pieceSet: pieceTheme === 'default' ? 'merida' : pieceTheme,
          coordinates,
        })
        setSvg(res.svg)
        setEvalData(res.eval)
      } catch {
        setError('Could not generate board')
      } finally {
        setLoading(false)
      }
    },
    [activeFen, boardTheme, pieceTheme, coordinates],
  )

  // Whenever the board config changes while showing the generated board, refresh.
  const firstGen = useRef(true)
  useEffect(() => {
    if (mode !== 'generated') return
    regenerate()
    firstGen.current = false
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [mode, boardTheme, pieceTheme, coordinates, activeFen])

  const showGenerated = () => setMode('generated')
  const showOriginal = () => {
    setMode('original')
    setMenuOpen(false)
    setEditingFen(false)
  }

  const onContextMenu = (e: React.MouseEvent) => {
    e.preventDefault()
    setMenuOpen(true)
  }

  const applyFen = () => {
    setActiveFen(fenDraft)
    setEditingFen(false)
  }

  return (
    <span className="diagram-image relative inline-block">
      {/* Toggle control */}
      <button
        type="button"
        onClick={mode === 'original' ? showGenerated : showOriginal}
        aria-label={mode === 'original' ? 'Show generated board' : 'Show original image'}
        title={mode === 'original' ? 'Show generated board' : 'Show original image'}
        className="absolute top-1 right-1 z-10 flex h-7 w-7 items-center justify-center rounded-md border border-slate-300 bg-white/90 text-slate-700 shadow-sm transition hover:bg-white"
      >
        {mode === 'original' ? (
          <svg className="h-4 w-4" viewBox="0 0 16 16" fill="currentColor" aria-hidden="true">
            <rect x="0" y="0" width="4" height="4" /><rect x="8" y="0" width="4" height="4" />
            <rect x="4" y="4" width="4" height="4" /><rect x="12" y="4" width="4" height="4" />
            <rect x="0" y="8" width="4" height="4" /><rect x="8" y="8" width="4" height="4" />
            <rect x="4" y="12" width="4" height="4" /><rect x="12" y="12" width="4" height="4" />
          </svg>
        ) : (
          <svg className="h-4 w-4" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" aria-hidden="true">
            <rect x="3" y="3" width="18" height="18" rx="2" />
            <circle cx="9" cy="9" r="2" />
            <path d="M21 15l-5-5L5 21" />
          </svg>
        )}
      </button>

      {mode === 'original' ? (
        <img src={src} alt={alt} className="max-w-full h-auto" />
      ) : (
        <span className="inline-block">
          <span
            data-testid="diagram-generated"
            role="img"
            aria-label={`${alt} (generated)`}
            onContextMenu={onContextMenu}
            className="block"
            // eslint-disable-next-line react/no-danger
            dangerouslySetInnerHTML={{ __html: svg ?? '' }}
          />
          {loading && <span className="block text-[10px] text-slate-400">Generating…</span>}
          {error && <span className="block text-[10px] text-red-500">{error}</span>}
          {evalData && !error && (
            <span className="block font-mono text-[10px] leading-tight text-slate-500">
              {formatDiagramEvalFooter(evalData, activeFen)}
            </span>
          )}

          {menuOpen && (
            <DiagramConfigMenu
              boardTheme={boardTheme}
              pieceTheme={pieceTheme}
              coordinates={coordinates}
              onBoardTheme={(v) => setSetting({ diagramBoardTheme: v })}
              onPieceTheme={(v) => setSetting({ diagramPieceTheme: v })}
              onCoordinates={(v) => setSetting({ diagramCoordinates: v })}
              onEditFen={() => {
                setFenDraft(activeFen)
                setEditingFen(true)
              }}
              onClose={() => setMenuOpen(false)}
            />
          )}

          {editingFen && (
            <span className="mt-1 flex items-center gap-1">
              <label className="text-[10px] text-slate-500" htmlFor="diagram-fen-input">
                FEN
              </label>
              <input
                id="diagram-fen-input"
                value={fenDraft}
                onChange={(e) => setFenDraft(e.target.value)}
                className="w-64 rounded border border-slate-300 px-1 py-0.5 font-mono text-[10px]"
              />
              <button
                type="button"
                onClick={applyFen}
                className="rounded border border-emerald-500 px-1.5 text-[10px] text-emerald-600 hover:bg-emerald-50"
              >
                Apply FEN
              </button>
            </span>
          )}
        </span>
      )}
    </span>
  )
}

interface MenuProps {
  boardTheme: BoardTheme
  pieceTheme: PieceTheme
  coordinates: boolean
  onBoardTheme: (v: BoardTheme) => void
  onPieceTheme: (v: PieceTheme) => void
  onCoordinates: (v: boolean) => void
  onEditFen: () => void
  onClose: () => void
}

function DiagramConfigMenu({
  boardTheme,
  pieceTheme,
  coordinates,
  onBoardTheme,
  onPieceTheme,
  onCoordinates,
  onEditFen,
  onClose,
}: MenuProps) {
  return (
    <span
      role="menu"
      aria-label="Diagram settings"
      className="absolute z-20 mt-1 flex flex-col gap-1.5 rounded-md border border-slate-200 bg-white p-2 text-[11px] shadow-lg"
    >
      <label className="flex items-center justify-between gap-2">
        <span>Board theme</span>
        <select
          aria-label="Board theme"
          value={boardTheme}
          onChange={(e) => onBoardTheme(e.target.value as BoardTheme)}
          className="rounded border border-slate-300 px-1"
        >
          {Object.entries(BOARD_THEMES).map(([key, t]) => (
            <option key={key} value={key}>{t.label}</option>
          ))}
        </select>
      </label>

      <label className="flex items-center justify-between gap-2">
        <span>Piece set</span>
        <select
          aria-label="Piece set"
          value={pieceTheme}
          onChange={(e) => onPieceTheme(e.target.value as PieceTheme)}
          className="rounded border border-slate-300 px-1"
        >
          {Object.entries(PIECE_THEMES).map(([key, t]) => (
            <option key={key} value={key}>{t.label}</option>
          ))}
        </select>
      </label>

      <label className="flex items-center justify-between gap-2">
        <span>Coordinate symbols</span>
        <input
          type="checkbox"
          aria-label="Coordinate symbols"
          checked={coordinates}
          onChange={(e) => onCoordinates(e.target.checked)}
        />
      </label>

      <span className="mt-1 flex items-center justify-between gap-2">
        <button
          type="button"
          onClick={onEditFen}
          className="rounded border border-slate-300 px-1.5 py-0.5 hover:bg-slate-50"
        >
          Edit FEN
        </button>
        <button
          type="button"
          onClick={onClose}
          className="rounded px-1.5 py-0.5 text-slate-400 hover:text-slate-700"
        >
          Close
        </button>
      </span>
    </span>
  )
}
